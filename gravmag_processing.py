def gn_84(lat):
    """
    Normal gravity calculated with WGS84 gravity formula [mGal]    
      
    Ref:
    - Decker, B. L. (1986). World geodetic system 1984
    """ 
    
    a = 978032.53359 
    b = 0.00193185138639
    c = 0.00669437999013
    lat = np.deg2rad(lat)
    gna = 1+b*np.power(np.sin(lat),2)
    gnb = np.sqrt(1-c*np.power(np.sin(lat),2))
    gnc = a*gna/gnb
    
    return gnc
    
