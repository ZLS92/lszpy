def gn_67(lat):
    """
    Normal gravity calculated with the International Gravity Formula 1967 [mGal].
    Based on Hayford Elipsoid     
    """  
    
    a = 978031.846
    b = 0.005278895
    c = 0.000023462
    gn1 = b*np.power(np.sin(np.deg2rad(lat)),2)
    gn2 = +c*np.power(np.sin(np.deg2rad(lat)),4)
    gn3 = a*(1+gn1+gn2)
    
    return gn3

