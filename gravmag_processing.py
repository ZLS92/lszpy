def g_shpere( dist=0, radius=0, density=None, mass=None ) :
    
    if mass is None :
        
        volume = ( 4/3 )  * np.pi * radius**3
        mass = density * volume
        
    g_sph = G * mass / ( ( dist + radius )**2 )

    return g_sph
    
