def atm_c(h):
    """
    Atmospheric correction [mGal] 
    Ref:
    - Hinze et all., 2005 doi:10.1190/1.1988183
    """  

    a = 0.874
    b = 9.9*(10**(-5))
    c = 3.56*(10**(-9))
    gnb = b*h
    gnc = c*np.power(h,2)
    
    return a - gnb + gnc

